        //byte
        byte byteVariable = 120;
        System.out.println(byteVariable);

        //short
        short salary = 2000;
        System.out.println(salary);

        //char
        char letterA = 'A';

        //int
        int populationInLatvia = 2000000;
        System.out.println(populationInLatvia);

        //long
