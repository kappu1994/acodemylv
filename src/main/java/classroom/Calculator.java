package classroom;

public class Calculator {

    //create 4 methods for each operation

    public int sum(int x,int y) {
        return x  + y;
    }

    public int multiply(int x,int y) {
        return x * y;
    }

    public int divide(int x, int y) {
        return x / y;
    }

    public int minus(int x, int y) {
        return x - y;

    }

    public int square (int x) {
        return x * x;
    }
}
